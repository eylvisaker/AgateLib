using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace AgateLib.InputLib
{

	/// <summary>
	/// Mouse Buttons enum.
	/// </summary>
	public enum MouseButton
	{
		/// <summary>
		/// No mouse button
		/// </summary>
		None,
		/// <summary>
		/// Primary button, typically the left button.
		/// </summary>
		Primary,
		/// <summary>
		/// Secondary button, typically the right button.
		/// </summary>
		Secondary,
		/// <summary>
		/// Middle button on some mice.
		/// </summary>
		Middle,
		/// <summary>
		/// First Extra Button
		/// </summary>
		ExtraButton1,
		/// <summary>
		/// Second Extra Button
		/// </summary>
		ExtraButton2,
		/// <summary>
		/// Third Extra Button
		/// </summary>
		ExtraButton3,
	}
}
